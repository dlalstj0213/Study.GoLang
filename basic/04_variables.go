package main

import (
	"fmt"
)

var name, gender string
var age int
var isStudent bool

func main() {
	var i int
	fmt.Println("i:", i)
	fmt.Println("name:", name)
	fmt.Println("gender:", gender)
	fmt.Println("age:", age)
	fmt.Println("isStudent:", isStudent)
}

